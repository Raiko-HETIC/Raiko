<?php

namespace App\Entity;

class Category extends BaseEntity
{
    private int $id;
    private int $name;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     * @return Category
     */
    public function setId(int $id): Category
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return Category
     */
    public function setDate(string $name): Category
    {
        $this->name = $name;
        return $this;
    }
}
